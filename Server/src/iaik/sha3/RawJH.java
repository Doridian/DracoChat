  public RawJH(int digestLength, int blockSize) {
    super("JH" + (digestLength << 3), digestLength, blockSize);
  }

  @Override
  void doPadding() {
    final long bitcount = count_ << 3;

    for (int i = 0; i < bits_.length; i++) {
      bits_[7 - i] = (byte) (bitcount >>> (i << 3));
    }

    final int remainder = (int) (count_ & 0x3fL);

    if (remainder != 0) {
      engineUpdate(padding_, 0, (blockSize_ << 1) - remainder - bits_.length);
    } else {
      engineUpdate(padding_, 0, blockSize_ - bits_.length);
    }

    engineUpdate(bits_, 0, bits_.length);
  }

  @Override
  protected void engineReset() {
    count_ = 0;
    Util.zeroBlock(buffer_);
    Util.zeroBlock(bits_);
  }
